
package javaapplication1;


public class UnaryOperator {
    
    
    public static void main(String[] args) {
        
        int x = -10;
        int result;
        
        // +a;
        
        result = +x; /// -10
        System.out.println(result);
        
        result = -x;  /// 10
        System.out.println(result);
        
        
        
        
    }
    
    
}
